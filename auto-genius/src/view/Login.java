    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        if(txtLogin.getText().isEmpty() || txtSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo vazio\n Verifique e tente novamente", "Erro", 0);
        }else{
            MetodosLogin fun = new MetodosLogin();
            
            if(fun.logar(txtLogin.getText(), txtSenha.getText()) == 0){
                dispose();
                
                Menu x = new Menu();
                x.setExtendedState(JFrame.MAXIMIZED_BOTH);
                x.setVisible(true);                
            }
        }
    }//GEN-LAST:event_btnLogarActionPerformed

    
